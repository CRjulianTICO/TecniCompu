    private void VerificaClienteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VerificaClienteBtnActionPerformed
        try {
            if (!CedulaClienteTxt.getText().isEmpty()) {
                MantenimientoCliente mc = MantenimientoCliente.getInstancia();
                int resVerifica = mc.verificaCliente(CedulaClienteTxt.getText());
                System.out.println(""+CedulaClienteTxt.getText());
                if ( resVerifica == 1) {
                    generaTableCliente(mc.listarCliente(CedulaClienteTxt.getText()));
                }else{
                    int resultado = JOptionPane.showConfirmDialog(rootPane, "Cliente no registrado. \n Desea Agregarlo?");
                    if(resultado==JOptionPane.YES_OPTION){
                        ClienteJF cjf = new ClienteJF();
                        cjf.show();
                    }
                }
            }else{
                JOptionPane.showMessageDialog(rootPane, "Llene el camopo solicitado");
            }
        } catch(NumberFormatException ne){
            System.out.println(""+ne);
        }catch (Exception e) {
            System.out.println(""+e);
        }
    }//GEN-LAST:event_VerificaClienteBtnActionPerformed

