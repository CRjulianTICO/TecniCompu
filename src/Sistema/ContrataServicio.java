    private void ContrataServicioBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ContrataServicioBtnActionPerformed
        try{
        if (!(CedulaClienteTxt.getText().isEmpty()||CedulaEmpleadoTxt.getText().isEmpty()||CodigoServicioTxt.getText().isEmpty())) {
            MantenimientoCliente mc = MantenimientoCliente.getInstancia();
            mc.insertaServicioCliente(Integer.parseInt(CodigoServicioTxt.getText()), CedulaClienteTxt.getText(), CedulaEmpleadoTxt.getText());
            limpiar();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Llene los campos solicitados");
        }
        }catch(NumberFormatException ne){
            JOptionPane.showMessageDialog(rootPane, "En los campos que se le solicitan los codigos solo puede insertar numeros");
            System.out.println(""+ne);
        }catch(Exception e){
            System.out.println(""+e);
        }
    }//GEN-LAST:event_ContrataServicioBtnActionPerformed

